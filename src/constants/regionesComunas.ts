// Helper function to get comunas by region
export const getComunasByRegion = (region: string): string[] => {
  const regionData = REGIONES_COMUNAS.find(r => r.region === region);
  return regionData ? regionData.comunas : [];
};

// Get all region names
export const getAllRegiones = (): string[] => {
  return REGIONES_COMUNAS.map(r => r.region);
};
